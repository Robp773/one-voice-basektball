        <div class="philosophy-section__group">
            <h3 class='page__h3'>OFFENSIVE PHILOSOPHY</h3>
            <p>We expect every player on the court to be a threat to the defense. For us that means on the catch every player faces up, bringing their eyes to the rim, and challenges their defender. It can be as simple as a jab step or one or two dribbles.  We expect every player to challenge the defense every time they touch the ball. When they do that all the off-ball defenders have to be aware of their help responsibilities. A single dribble can result in a help defender adjusting to help. It can get the defense to move. Whenever you get the defense to move you will create better offensive opportunities. Attention to detail (such as proper use of a screen, hard cuts) and discipline (running your schemes through until you create the best scoring opportunity) allow teams with less offensive skill to compete.</p>
            <p>Conversely, when players simply catch and pass, often without even looking at the basket, the offense is basically playing 4 on 5. The same is true for players who catch a pass and immediately raise the ball overhead reducing triple threat opportunities to single threat (passing) opportunities. Passive players allow the defense to relax, sag into the passing lanes or maintain their desired spacing and react quicker in help situations. We want the defense to be aware of the player with the ball, forced to split their attention on the ball and their assigned player and put them in a situation that requires help and recover / close outs. Help, recover and close out situations often lead to better attack angles for the offensive player and the breakdown of defensive structure – more space, more offensive scoring opportunities.</p>
        </div>

        <div class="philosophy-section__group">
            <h3 class="page__h3">ZONE OFFENSE – FOCUS POINTS</h3>
            <p>Attack the gaps in the zone – with the dribble, flashing into the gaps for a pass or positioning post players in the gaps – to occupy the defense.</p>
            <p>Move the defense; ball reversal – either through the high post or passing around the perimeter.</p>
            <p>On ball reversal – challenge the defense. For example, if the ball goes into the high post, the post player must face up. This holds the defense in the lane and slows their response to anticipated passes to the wings.</p>
            <p>On ball reversal – pass on the perimeter to reverse the ball. Every player must 1st attack the gap in front of them before passing. This again slows the defense’s response to anticipated passes. When the ball reaches the top the guard must “freeze” the elbow defender by dribbling at her before passing to the wing. If we fail to freeze the elbow defender it allows the defender to anticipate the pass, stepping into the passing lane between the top and the wing. An intercepted pass or, in a best case scenario, a high, slow pass to the wing results.</p>
        </div>

